package com.example.graduation1;

public class Jihwa {
    String name;
    int jihwaImg;

    public Jihwa(String name, int jihwaImg){
        this.name = name;
        this.jihwaImg = jihwaImg;
    }

    public String getName(){
        return name;
    }

    public void setName(String name){
        this.name=name;
    }

    public int getJihwaImg(){
        return jihwaImg;
    }
    public void setJihwaImg(int jihwaImg){
        this.jihwaImg = jihwaImg;
    }
}
